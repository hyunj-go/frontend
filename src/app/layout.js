import Link from 'next/link';
import './globals.css'

export const metadata = {
  title: 'WEB',
  description: 'Generated by hyunj',
}
// let bakeries;
export default async function RootLayout({ children }) {
    const resp = await fetch(`${process.env.API_URL}/api/bakeries`);
    const bakeries = await resp.json();
    
    
    // axios.get('http://127.0.0.1:1337/api/reviews').then(resp => {
    //   bakeries = resp.data;
    // });
    
  return (
    <html>
      <body>
        <h1><a href='/'>WEB</a></h1>
        <ol>
          {bakeries.data.map((bakery)=>{
            return <li key={bakery.id}><Link href={`/read/${bakery.id}`}>{bakery.attributes.name}</Link></li>
          })}
        </ol>
        {children}
      </body>
    </html>
  )
}
